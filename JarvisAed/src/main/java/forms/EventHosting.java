    public ArrayList<Events_hosted> eventList() {
        ArrayList<Events_hosted> eventList = new ArrayList<>();
        try {
            Connection conn = DbUtil.getConnection();
            // String query1 = "Select Property_id, PropertyName, Status FROM property_enlist ";

            PreparedStatement prep1 = conn.prepareStatement(IQueryMapper.SELECT_EVENTS_HOSTED_3); //add date later
            //PreparedStatement pst = conn.prepareStatement(updateQuery);
            prep1.setString(1, "%"+jTextField1.getText()+"%");
            prep1.setString(2, jComboBox1.getSelectedItem().toString());
           // prep1.setString(3,jTextField2.getText());

            ResultSet rs = null;
            rs = prep1.executeQuery();
            Events_hosted events;
            while (rs.next()) {
                events = new Events_hosted(rs.getString("EventID"), rs.getString("Event_Name"), rs.getString("Organizer"), rs.getString("Address"), rs.getString("Food"), rs.getInt("Available_Ticket"));
                eventList.add(events);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return eventList;
    }

    public void populate_table_events() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        ArrayList<Events_hosted> list = eventList();

        Object[] row = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            if(list.get(i).getAvailable_tickets()>2){
                
            
            row[0] = list.get(i).getEventID();
            row[1] = list.get(i).getEvent_Name();
            row[2] = list.get(i).getOrganizer();
            row[3] = list.get(i).getAddress();
            row[4] = list.get(i).getAvailable_tickets();
            model.addRow(row);
            }
        }
    }

