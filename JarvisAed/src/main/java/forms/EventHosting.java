    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = jTable1.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a record to view");
            return;
        }
        jButton2.setVisible(true);
        String food = null;
        String EventID = (jTable1.getModel().getValueAt(selectedRowIndex, 0).toString());
        try {
            Connection conn = DbUtil.getConnection();
            // String query1 = "Select Property_id, PropertyName, Status FROM property_enlist ";

            PreparedStatement prep1 = conn.prepareStatement(IQueryMapper.SELECT_EVENTS_HOSTED_2);
            //PreparedStatement pst = conn.prepareStatement(updateQuery);
            prep1.setString(1, EventID);
            ResultSet rs = null;
            rs = prep1.executeQuery();
            if (rs.next()) {
                food = rs.getString(1);
            }

            PreparedStatement prep2 = conn.prepareStatement(IQueryMapper.SELECT_EVENTS_IMAGE);
            //PreparedStatement pst = conn.prepareStatement(updateQuery);

            prep2.setString(1, EventID);

            rs = prep2.executeQuery();
            byte[] event_img1 = null;
            if (rs.next()) {
                event_img1 = rs.getBytes("Img_link");
                event_img2= rs.getBytes("Img_link");
            }
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(event_img1).getImage().getScaledInstance(jLabel8.getWidth(), jLabel8.getHeight(), Image.SCALE_SMOOTH));
            jLabel15.setIcon(imageIcon);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        jLabel10.setText(model.getValueAt(selectedRowIndex, 0).toString());
        jLabel11.setText(model.getValueAt(selectedRowIndex, 1).toString());
        jLabel12.setText(model.getValueAt(selectedRowIndex, 2).toString());
        jLabel13.setText(model.getValueAt(selectedRowIndex, 3).toString());
        jLabel14.setText(food);

    }//GEN-LAST:event_jButton3ActionPerformed

