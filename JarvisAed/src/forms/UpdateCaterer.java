    public ArrayList<Caterer> catererList (){
           ArrayList<Caterer> catererList = new ArrayList<>();
           try {
            Connection conn = DbUtil.getConnection();
           // String query1 = "Select Property_id, PropertyName, Status FROM property_enlist ";
           
            PreparedStatement prep1 = conn.prepareStatement(IQueryMapper.SELECT_CATERER);
         //PreparedStatement pst = conn.prepareStatement(updateQuery);
            ResultSet rs = null;
            rs= prep1.executeQuery();
            Caterer caterer;
            while (rs.next()){
            caterer = new Caterer(rs.getString("CatererID"), rs.getString("CatererName"), rs.getString("Address"), rs.getString("Phone"), rs.getString("Ratings"));    
            catererList.add(caterer);
            }
            
        }
           catch (Exception e){
               JOptionPane.showMessageDialog(null,e);
           }
           return catererList;
    }
    
    public void show_caterers(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        ArrayList <Caterer> list = catererList();
        
        Object[] row = new Object[5];
        for (int i =0; i<list.size(); i++)
        {
            row[0] = list.get(i).getCatererID();
            row[1]= list.get(i).getCaterer_Name();
            row[2] = list.get(i).getAddress();
            row[3]= list.get(i).getPhone();
            row[4] = list.get(i).getRatings();
            model.addRow(row);
        }    
    }

