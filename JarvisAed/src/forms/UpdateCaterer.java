    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = DbUtil.getConnection();
            int row = jTable1.getSelectedRow();
            if (row<0){
            JOptionPane.showMessageDialog(this, "Please select a record to view");
        }
         String CatererID = (jTable1.getModel().getValueAt(row, 0).toString());   
         //String updateQuery = "UPDATE property_enlist SET PropertyName=?, Capacity = ? where Property_id ="+PropertyID;
         if(jTextField1.getText().isBlank() || jTextField2.getText().isBlank() || jTextArea1.getText().isBlank())
         {
             JOptionPane.showMessageDialog(null, "Please fill all the details before updating");
             return;
         }
         
         PreparedStatement prep = conn.prepareStatement(IQueryMapper.UPDATE_CATERER);
         prep.setString(1, jTextField2.getText());
         prep.setString(2, jTextArea1.getText());
         prep.setString(3, jTextField1.getText());
         prep.setString(4, jLabel6.getText());
          
          prep.executeUpdate();
          JOptionPane.showMessageDialog(null,"Updated Successfully");
          
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
          model.setRowCount(0);
          show_caterers();
          jTextField1.setText("");
          jTextField2.setText("");
          jTextArea1.setText("");
          jLabel6.setText("");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

